//For Up 1 Button//
var count11 = 0
var button11 = document.getElementById('buttoncount11');
var display11 = document.getElementById('up1');

button11.onclick=function(){
    count11++;
    display11.innerHTML=count11;
}

//For Down 1 Button//
var count12 = 0
var button12 = document.getElementById('buttoncount12');
var display12 = document.getElementById('dn1');

button12.onclick=function(){
    count12--;
    display12.innerHTML=count12;
}

